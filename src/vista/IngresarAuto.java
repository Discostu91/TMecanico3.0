 private void cargarComboBox(){
 
        try{
            DefaultComboBoxModel modelocomboauto = new DefaultComboBoxModel();
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            Connection conexion = DriverManager.getConnection("jdbc:derby://localhost:1527/tallermecanico", "root", "roottaller");
            Statement st = conexion.createStatement();
            ResultSet rs = st.executeQuery("SELECT id_modelo, descripcion FROM modelo");
            modelocomboauto.addElement("Seleccione");
            while (rs.next()) {
                modelocomboauto.addElement(rs.getObject("descripcion"));
            }
            rs.close();
            this.comboauto.setModel(modelocomboauto);
        }catch(SQLException e){
         System.out.println(e);
    }   catch (ClassNotFoundException ex) {
            Logger.getLogger(MantencionRepuesto.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
